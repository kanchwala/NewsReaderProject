package ArticleBean;

import android.graphics.Bitmap;


public class Article {

	
	private String articleTitle;
	private String articleSummary;
	private String articleDate;
	private String articleAuthor;
	private String articleSource;
	private String articleLink;
	private Bitmap articleImage;
	private String articleImageLink;
	private String articlemediaType;
	
	
	
	public String getArticleTitle() {
		return articleTitle;
	}
	public void setArticleTitle(String articleTitle) {
		this.articleTitle = articleTitle;
	}
	public String getArticleSummary() {
		return articleSummary;
	}
	public void setArticleSummary(String articleSummary) {
		this.articleSummary = articleSummary;
	}
	public String getArticleDate() {
		return articleDate;
	}
	public void setArticleDate(String articleDate) {
		this.articleDate = articleDate;
	}
	public String getArticleAuthor() {
		return articleAuthor;
	}
	public void setArticleAuthor(String articleAuthor) {
		this.articleAuthor = articleAuthor;
	}
	public String getArticleSource() {
		return articleSource;
	}
	public void setArticleSource(String articleSource) {
		this.articleSource = articleSource;
	}
	public String getArticleLink() {
		return articleLink;
	}
	public void setArticleLink(String articleLink) {
		this.articleLink = articleLink;
	}
	public Bitmap getArticleImage() {
		return articleImage;
	}
	public void setArticleImage(Bitmap articleImage) {
		this.articleImage = articleImage;
	}
	public String getArticleImageLink() {
		return articleImageLink;
	}
	public void setArticleImageLink(String articleImageLink) {
		this.articleImageLink = articleImageLink;
	}
	public String getArticlemediaType() {
		return articlemediaType;
	}
	public void setArticlemediaType(String articlemediaType) {
		this.articlemediaType = articlemediaType;
	}
	
	
}
